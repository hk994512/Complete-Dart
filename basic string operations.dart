void main(){
  String dart = 'Flutter and Dart';
  // Converting to Upper Case 
  print(dart.toUpperCase());
  
  // Similarly we can also convert it to UpperCase 
 String x = 'Hi Dart'.toUpperCase();
  print(x);
  
  // Converting literals to lower case 
  String toLower = 'HAMZA'.toLowerCase();
  print(toLower);
  
}
